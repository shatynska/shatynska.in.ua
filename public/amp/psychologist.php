<h2>психолог</h2>
<p>
Олена Шатинська, 41 рік, психолог та гештальт-терапевт в Дрогобичі. <br/>У 1998-2003рр. навчалася за спеціальністю "Психологія" в Дрогобицькому державному педагогічному університет імені Івана Франка.<br/>
2003-2019рр. працювала викладачем кафедри практичної психології Дрогобицького державного педагогічного університету імені Івана Франка.<br/>
З 2014р розпочала приватну психологічну практику.<br/>
2014-2015рр. проходила спеціалізацію по арт-терапії, включно з пісковою терапію та казкотерапією.<br/>
2015-2021рр. навчання гештальт-терапії в Українському Гештальт Інституті.<br/>
2018-2019рр. навчання на спеціалізації "Робота з кризами і травмою в гештальт-терапії",<br/>
2019-2020рр. навчання на спеціалізації "Робота з парою в гештальт-терапії".<br/>
Проходжу на постійній основі особисту терапію та супервізію.
